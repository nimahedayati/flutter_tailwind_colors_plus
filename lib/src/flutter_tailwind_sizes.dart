part of flutter_tailwind_theme;

/// A utility class for defining common border radius values.
class TBorderRadius {
  /// No border radius.
  static BorderRadius none = BorderRadius.zero;

  /// Small border radius.
  static BorderRadius sm = BorderRadius.circular(2);

  /// Normal border radius.
  static BorderRadius normal = BorderRadius.circular(4);

  /// Medium border radius.
  static BorderRadius md = BorderRadius.circular(6);

  /// Large border radius.
  static BorderRadius lg = BorderRadius.circular(8);

  /// Extra large border radius.
  static BorderRadius xl = BorderRadius.circular(12);

  /// Double extra large border radius.
  static BorderRadius xl2 = BorderRadius.circular(16);

  /// Triple extra large border radius.
  static BorderRadius xl3 = BorderRadius.circular(24);

  /// Full border radius.
  ///
  /// Use this for a fully rounded border.
  static BorderRadius full = BorderRadius.circular(9999);
}
